package edu.ou.activitysyncdataservice.common.mapper;

import edu.ou.activitysyncdataservice.data.EmotionDocument;
import org.mapstruct.Mapper;
import org.mapstruct.Mapping;
import org.mapstruct.Named;
import org.mapstruct.factory.Mappers;

import java.util.Map;

@Mapper
public interface EmotionDocumentMapper {
    EmotionDocumentMapper INSTANCE = Mappers.getMapper(EmotionDocumentMapper.class);

    /**
     * Map HashMap<String, String> object to EmotionDocument object
     *
     * @param map represents for EmotionAddRequest object
     * @return EmotionDocument object
     * @author Nguyen Trung Kien - OU
     */
    @Mapping(target = "id", ignore = true)
    @Mapping(target = "OId", source = "id", qualifiedByName = "objectToInt")
    @Mapping(target = "name", source = "name", qualifiedByName = "objectToString")
    @Mapping(target = "icon", source = "icon", qualifiedByName = "objectToString")
    EmotionDocument fromMap(Map<String, Object> map);

    /**
     * Convert object to String
     *
     * @param object object will be converted
     * @return String object
     * @author Nguyen Trung Kien - OU
     */
    @Named("objectToString")
    static String objectToString(Object object) {
        return (String) object;
    }

    /**
     * Convert object to in
     *
     * @param object object will be converted
     * @return int value
     * @author Nguyen Trung Kien - OU
     */
    @Named("objectToInt")
    static int objectToInt(Object object) {
        return (int) object;
    }
}
